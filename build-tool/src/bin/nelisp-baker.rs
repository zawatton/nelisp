fn main() -> ExitCode {
    let mut args = env::args().skip(1);
    let mut lisp_dir: Option<PathBuf> = None;
    let mut check_only = false;
    let mut frozen_heap = false;
    let mut verify_fixtures: Vec<PathBuf> = Vec::new();
    let mut in_verify_mode = false;
    while let Some(arg) = args.next() {
        match arg.as_str() {
            "--lisp-dir" => match args.next() {
                Some(v) => lisp_dir = Some(PathBuf::from(v)),
                None => {
                    eprintln!("nelisp-baker: --lisp-dir requires an argument");
                    return ExitCode::from(2);
                }
            },
            "--check" => check_only = true,
            "--frozen-heap" => frozen_heap = true,
            "--verify-elisp-fixtures" => {
                in_verify_mode = true;
                // Remaining args are fixture paths.
                while let Some(path) = args.next() {
                    verify_fixtures.push(PathBuf::from(path));
                }
            }
            "--help" | "-h" => {
                println!(
                    "usage: nelisp-baker [--lisp-dir DIR] [--check] [--frozen-heap]\n\
                          nelisp-baker --verify-elisp-fixtures FIXTURE [FIXTURE...]"
                );
                return ExitCode::SUCCESS;
            }
            other => {
                eprintln!("nelisp-baker: unknown argument {}", other);
                return ExitCode::from(2);
            }
        }
    }
    // Doc 95 §95.e cross-impl byte-identity gate: decode each elisp-
    // produced fixture and assert the Rust encoder yields identical
    // bytes for the same fallback-form payload.
    if in_verify_mode {
        if verify_fixtures.is_empty() {
            eprintln!(
                "nelisp-baker: --verify-elisp-fixtures needs >=1 fixture path"
            );
            return ExitCode::from(2);
        }
        let mut mismatches = Vec::new();
        for fixture in &verify_fixtures {
            match verify_elisp_fixture(fixture) {
                Ok(()) => {
                    println!("verified {} (byte-identical)", fixture.display());
                }
                Err(msg) => {
                    eprintln!("nelisp-baker: {} : {}", fixture.display(), msg);
                    mismatches.push(fixture.clone());
                }
            }
        }
        return if mismatches.is_empty() {
            ExitCode::SUCCESS
        } else {
            ExitCode::from(4)
        };
    }
    let lisp_dir = match lisp_dir {
        Some(p) => p,
        None => default_lisp_dir(),
    };
    if !lisp_dir.is_dir() {
        eprintln!(
            "nelisp-baker: lisp dir does not exist: {}",
            lisp_dir.display()
        );
        return ExitCode::from(1);
    }

    let mut mismatches = Vec::new();
    // Doc 98 §98.2 — `--frozen-heap' switches to the iterative driver
    // that shares ONE `Env::new_global_no_stdlib' across all
    // STDLIB_FILES so each emitted image carries only the diff of
    // that file's contribution (= proper frozen-heap node-pool, no
    // FALLBACK_FORMS section).  Without the flag, the legacy per-
    // file form-shim path (`compile_elisp_to_image' = source stashed
    // in FALLBACK_FORMS) is retained for backward compatibility.
    let mut frozen_env: Option<Env> = if frozen_heap {
        Some(Env::new_global_no_stdlib())
    } else {
        None
    };
    for name in STDLIB_FILES {
        let src_path = lisp_dir.join(name);
        let img_path = lisp_dir.join(format!("{}.image", name));
        let source = match fs::read_to_string(&src_path) {
            Ok(s) => s,
            Err(e) => {
                eprintln!("nelisp-baker: cannot read {}: {}", src_path.display(), e);
                return ExitCode::from(1);
            }
        };
        let bytes = if let Some(env) = frozen_env.as_mut() {
            match image::iterative_bake_one(env, &source) {
                Ok(b) => b,
                Err(e) => {
                    eprintln!("nelisp-baker: frozen-heap bake {} failed: {}", name, e);
                    return ExitCode::from(7);
                }
            }
        } else {
            match image::compile_elisp_to_image(&source) {
                Ok(b) => b,
                Err(e) => {
                    eprintln!("nelisp-baker: bake {} failed: {}", name, e);
                    return ExitCode::from(7);
                }
            }
        };
        if check_only {
            let on_disk = fs::read(&img_path).unwrap_or_default();
            if on_disk != bytes {
                mismatches.push(name.to_string());
            }
        } else if let Err(e) = fs::write(&img_path, &bytes) {
            eprintln!("nelisp-baker: cannot write {}: {}", img_path.display(), e);
            return ExitCode::from(1);
        } else {
            println!("baked {} ({} bytes)", img_path.display(), bytes.len());
        }
    }

    if check_only && !mismatches.is_empty() {
        eprintln!("nelisp-baker: stale images detected:");
        for n in &mismatches {
            eprintln!("  {}.image", n);
        }
        eprintln!("run `make bake-images` to refresh.");
        return ExitCode::from(3);
    }
    ExitCode::SUCCESS
}

fn default_lisp_dir() -> PathBuf {
    // CARGO_MANIFEST_DIR is build-tool/, sibling lisp/ is one up.
    let manifest = env!("CARGO_MANIFEST_DIR");
    PathBuf::from(manifest).join("..").join("lisp")
}

/// Doc 95 §95.e: decode an elisp-produced NELIMG v3 fixture, re-emit
/// via the Rust encoder, and confirm byte-identity.  Returns Err with
/// a diagnostic string on any mismatch / decode error.
fn verify_elisp_fixture(path: &std::path::Path) -> Result<(), String> {
    let bytes = fs::read(path).map_err(|e| format!("read failed: {}", e))?;
    let mut env = Env::empty();
    let fallback_forms = image::decode_v3_into(&mut env, &bytes)
        .map_err(|e| format!("decode failed: {}", e))?;
    // Re-emit via Rust encoder using the same fallback-form payload
    // and an empty env (= matches the envelope-only subset produced
    // by `compile_elisp_to_image').
    let reemit = image::encode_v3_with_fallback(&Env::empty(), &fallback_forms)
        .map_err(|e| format!("re-encode failed: {}", e))?;
    if reemit == bytes {
        Ok(())
    } else {
        // Find first diverging byte for a more actionable report.
        let first_diff = bytes
            .iter()
            .zip(reemit.iter())
            .position(|(a, b)| a != b);
        Err(format!(
            "byte-mismatch (on-disk-len={} reemit-len={} first-diff={:?})",
            bytes.len(),
            reemit.len(),
            first_diff
        ))
    }
}
